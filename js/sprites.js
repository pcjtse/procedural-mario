  // ── Sprite Rendering ──
  var PixelArt = {};

  // Cache rendered sprites as ImageData for performance
  var spriteCache = {};

  // Pre-render a sprite string array to a canvas
  function renderSpriteToCanvas(spriteData, palette, flipX) {
    var h = spriteData.length;
    var w = spriteData[0].length;
    var canvas = document.createElement('canvas');
    canvas.width = w;
    canvas.height = h;
    var ctx = canvas.getContext('2d');

    for (var row = 0; row < h; row++) {
      var line = spriteData[row];
      for (var col = 0; col < w; col++) {
        var idx = parseInt(line[flipX ? (w - 1 - col) : col], 10);
        var color = palette[idx];
        if (color) {
          ctx.fillStyle = color;
          ctx.fillRect(col, row, 1, 1);
        }
      }
    }
    return canvas;
  }

  // Get or create a cached sprite canvas
  function getCachedSprite(name, spriteData, palette, flipX, starFrame) {
    var key = name + (flipX ? '_L' : '_R') + (starFrame >= 0 ? '_S' + starFrame : '');
    if (!spriteCache[key]) {
      var mergedPalette = palette;
      if (starFrame >= 0) {
        mergedPalette = {};
        for (var k in palette) {
          mergedPalette[k] = palette[k];
        }
        var starP = STAR_PALETTES[starFrame % STAR_PALETTES.length];
        for (var sk in starP) {
          mergedPalette[sk] = starP[sk];
        }
      }
      spriteCache[key] = renderSpriteToCanvas(spriteData, mergedPalette, flipX);
    }
    return spriteCache[key];
  }

  // Draw a named sprite at screen position
  PixelArt.drawSprite = function(ctx, spriteName, powerState, x, y, flipX, starFrame) {
    var isSmall = (powerState === 'small');
    var spriteSet = isSmall ? SMALL_SPRITES : BIG_SPRITES;
    var paletteSet = PALETTE[powerState] || PALETTE.small;
    var data = spriteSet[spriteName];

    if (!data) {
      data = spriteSet.idle; // fallback
    }

    var sf = (starFrame !== undefined && starFrame >= 0) ? starFrame : -1;
    var cached = getCachedSprite(
      (isSmall ? 's_' : 'b_') + spriteName + '_' + powerState,
      data,
      paletteSet,
      flipX,
      sf
    );

    ctx.drawImage(cached, Math.round(x), Math.round(y));
  };

  // Clear sprite cache (useful when palettes change)
  PixelArt.clearCache = function() {
    spriteCache = {};
  };

  // Get sprite dimensions
  PixelArt.getSpriteSize = function(powerState) {
    if (powerState === 'small') {
      return { w: 16, h: 16 };
    }
    return { w: 16, h: 32 };
  };

